SELECT CITY,SUM(ORDERS) AS ORDERS FROM DEMAND GROUP BY CITY ORDER BY ORDERS DESC

CREATE VIEW CITY AS
SELECT CITY AS PLACE,AVG(REVENUE) AS REVENUE,AVG(STOCK_AVAILABLE) 
AS STOCK,SUM(ORDERS) AS TOTAL_ORDERS FROM DEMAND GROUP BY CITY;

SELECT * FROM CITY
SELECT * FROM demand

SELECT PLACE,TOTAL_ORDERS FROM CITY WHERE TOTAL_ORDERS = (SELECT MAX(TOTAL_ORDERS) FROM CITY)
SELECT * FROM demand
ORDER BY ORDERS DESC;

SELECT TOP 10 CATEGORY, SUM(ORDERS) AS ORDERS FROM DEMAND
GROUP BY CATEGORY
ORDER BY ORDERS DESC

SELECT
    Date,
    SUM(Orders) AS Total_Orders
FROM DEMAND
GROUP BY Date
ORDER BY Date;

SELECT
    Category,
    SUM(Orders) AS Orders,
    SUM(Stock_Available) AS Stock,
    (SUM(Stock_Available)-SUM(ORDERS)) AS REMAIN
FROM DEMAND
GROUP BY Category;



